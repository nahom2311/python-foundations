## Types of Python Files

# 1 Numeric Types

# 1a int: Just regular integer numbers (no decimal numbers)
x = -5
print("Value of x:", x)
print("Type of x:", type(x))  # Output: <class 'int'>

# 1b float: Decimal numbers
y = 3.22
print("Value of y:", y)
print("Type of y:", type(y))  # Output: <class 'float'>
